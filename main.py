QUESTIONS_PER_DAY = 5
used_indices: list[int] = []

def get_daily_questions() -> list[dict]:
    global used_indices
    available = [i for i in range(len(QUESTIONS)) if i not in used_indices]
    if len(available) < QUESTIONS_PER_DAY:
        used_indices = []
        available = list(range(len(QUESTIONS)))
    selected = random.sample(available, QUESTIONS_PER_DAY)
    used_indices.extend(selected)
    return [QUESTIONS[i] for i in selected]

def format_question(q: dict, number: int) -> str:
    letters = ["А", "Б", "В", "Г"]
    options_text = "\n".join(f"{letters[i]}) {opt}" for i, opt in enumerate(q["options"]))
    correct_letter = letters[q["correct"]]
    return (
        f"❓ *Вопрос {number}*\n"
        f"{q['question']}\n\n"
        f"{options_text}\n\n"
        f"||✅ Правильный ответ: {correct_letter}\n{q['explanation']}||"
    )

async def send_daily_quiz(context: ContextTypes.DEFAULT_TYPE):
    questions = get_daily_questions()
    message = "📚 *Ежедневный квиз по макроэкономике*\n━━━━━━━━━━━━━━━━\n\n"
    for i, q in enumerate(questions, 1):
        message += format_question(q, i) + "\n\n"
    message += "_Нажми на спойлер чтобы увидеть ответ_ 👆"
    await context.bot.send_message(chat_id=GROUP_ID, text=message, parse_mode="MarkdownV2")

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("✅ MacroQuiz работает! Каждый день в 10:00 МСК — 5 вопросов по макроэкономике.\n\nКоманды:\n/quiznow — прислать вопросы прямо сейчас")

async def quiz_now(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await send_daily_quiz(context)

class HealthHandler(BaseHTTPRequestHandler):
    def do_GET(self):
        self.send_response(200)
        self.end_headers()
        self.wfile.write(b"OK")
    def log_message(self, format, *args):
        pass

def run_health_server():
    server = HTTPServer(("0.0.0.0", 5000), HealthHandler)
    server.serve_forever()

def main():
    threading.Thread(target=run_health_server, daemon=True).start()
    app = Application.builder().token(BOT_TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("quiznow", quiz_now))
    app.job_queue.run_daily(send_daily_quiz, time=time(hour=QUIZ_HOUR, minute=QUIZ_MINUTE, tzinfo=MOSCOW_TZ))
    logger.info("MacroQuiz запущен ✅")
    app.run_polling(drop_pending_updates=True)

if __name__ == "__main__":
    main()
