# --- Логика выдачи без повторов с зацикливанием ---
quiz_pool: list[int] = []


def _refill_pool():
    global quiz_pool
    quiz_pool = list(range(len(QUESTIONS)))
    random.shuffle(quiz_pool)
    logger.info("Пул вопросов перемешан заново (%d шт.)", len(quiz_pool))


def get_daily_questions() -> list[dict]:
    global quiz_pool
    selected = []
    for _ in range(QUESTIONS_PER_DAY):
        if not quiz_pool:
            _refill_pool()
        selected.append(quiz_pool.pop())
    return [QUESTIONS[i] for i in selected]


async def send_daily_quiz(context: ContextTypes.DEFAULT_TYPE):
    try:
        questions = get_daily_questions()
        await context.bot.send_message(
            chat_id=GROUP_ID,
            text="📚 Ежедневный квиз по макроэкономике — 5 вопросов 👇",
        )
        for q in questions:
            # Telegram: вариант ответа ≤ 100 симв., объяснение в poll ≤ 200 симв.
            await context.bot.send_poll(
                chat_id=GROUP_ID,
                question=q["question"][:300],
                options=[opt[:100] for opt in q["options"]],
                type="quiz",
                correct_option_id=q["correct"],
                is_anonymous=True,
                explanation=q["explanation"][:200],
            )
            await asyncio.sleep(1)  # чтобы не словить flood limit
        logger.info("Квиз из %d вопросов отправлен в группу %s", len(questions), GROUP_ID)
    except Exception as e:
        logger.error("Ошибка при отправке квиза: %s", e, exc_info=True)


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "✅ MacroQuiz работает! Каждый день в 10:00 МСК — 5 вопросов-викторин по макроэкономике.\n\n"
        "Команды:\n/quiznow — прислать вопросы прямо сейчас"
    )


async def quiz_now(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await send_daily_quiz(context)


class HealthHandler(BaseHTTPRequestHandler):
    def do_GET(self):
        self.send_response(200)
        self.end_headers()
        self.wfile.write(b"OK")

    def log_message(self, format, *args):
        pass


def run_health_server():
    port = int(os.environ.get("PORT", 5000))
    server = HTTPServer(("0.0.0.0", port), HealthHandler)
    logger.info("Health-сервер запущен на порту %s", port)
    server.serve_forever()


def main():
    threading.Thread(target=run_health_server, daemon=True).start()

    app = Application.builder().token(BOT_TOKEN).build()

    if app.job_queue is None:
        logger.error("JobQueue не установлен! Добавь 'python-telegram-bot[job-queue]' в requirements.txt")
        raise SystemExit(1)

    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("quiznow", quiz_now))

    app.job_queue.run_daily(
        send_daily_quiz,
        time=time(hour=QUIZ_HOUR, minute=QUIZ_MINUTE, tzinfo=MOSCOW_TZ),
    )

    logger.info("MacroQuiz запущен ✅ (%d вопросов в базе, квиз в %02d:%02d МСК)", len(QUESTIONS), QUIZ_HOUR, QUIZ_MINUTE)
    app.run_polling(drop_pending_updates=True)


if __name__ == "__main__":
    main()
